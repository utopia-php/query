    protected string $wrapChar = '"';

    #[\Override]
    public function table(string $name): Table\PostgreSQL
    {
        return new Table\PostgreSQL($this, $name);
    }

    protected function compileColumnType(Column $column): string
    {
        if ($column->userTypeName !== null) {
            return $this->quote($column->userTypeName);
        }

        return match ($column->type) {
            ColumnType::String, ColumnType::Varchar, ColumnType::Relationship => 'VARCHAR(' . ($column->length ?? 255) . ')',
            ColumnType::Text, ColumnType::MediumText, ColumnType::LongText => 'TEXT',
            ColumnType::Integer => 'INTEGER',
            ColumnType::BigInteger, ColumnType::Id => 'BIGINT',
            ColumnType::Float, ColumnType::Double => 'DOUBLE PRECISION',
            ColumnType::Boolean => 'BOOLEAN',
            ColumnType::Datetime => $column->precision ? 'TIMESTAMP(' . $column->precision . ')' : 'TIMESTAMP',
            ColumnType::Timestamp => $column->precision ? 'TIMESTAMP(' . $column->precision . ') WITHOUT TIME ZONE' : 'TIMESTAMP WITHOUT TIME ZONE',
            ColumnType::Json, ColumnType::Object => 'JSONB',
            ColumnType::Binary => 'BYTEA',
            ColumnType::Enum => 'TEXT',
            ColumnType::Point => 'GEOMETRY(POINT' . ($column->srid !== null ? ', ' . $column->srid : '') . ')',
            ColumnType::Linestring => 'GEOMETRY(LINESTRING' . ($column->srid !== null ? ', ' . $column->srid : '') . ')',
            ColumnType::Polygon => 'GEOMETRY(POLYGON' . ($column->srid !== null ? ', ' . $column->srid : '') . ')',
            ColumnType::Uuid7 => 'VARCHAR(36)',
            ColumnType::Vector => 'VECTOR(' . ($column->dimensions ?? 0) . ')',
            ColumnType::Serial => 'SERIAL',
            ColumnType::BigSerial => 'BIGSERIAL',
            ColumnType::SmallSerial => 'SMALLSERIAL',
            ColumnType::FixedString => throw new UnsupportedException('FixedString type is not supported in PostgreSQL.'),
        };
    }

    protected function compileAutoIncrement(): string
    {
        return 'GENERATED BY DEFAULT AS IDENTITY';
    }

    protected function compileUnsigned(): string
    {
        return '';
    }

    protected function compileColumnDefinition(Column $column): string
    {
        $parts = [
            $this->quote($column->name),
            $this->compileColumnType($column),
        ];

        if ($column->isUnsigned) {
            $unsigned = $this->compileUnsigned();
            if ($unsigned !== '') {
                $parts[] = $unsigned;
            }
        }

        if ($column->generatedExpression !== null) {
            $parts[] = $this->compileGeneratedClause($column);

            if (! $column->isNullable) {
                $parts[] = 'NOT NULL';
            } else {
                $parts[] = 'NULL';
            }

            if ($column->checkExpression !== null) {
                $parts[] = 'CHECK (' . $column->checkExpression . ')';
            }

            return \implode(' ', $parts);
        }

        $isSerial = $column->type === ColumnType::Serial
            || $column->type === ColumnType::BigSerial
            || $column->type === ColumnType::SmallSerial;

        if ($column->isAutoIncrement && ! $isSerial) {
            $parts[] = $this->compileAutoIncrement();
        }

        if (! $column->isNullable) {
            $parts[] = 'NOT NULL';
        } else {
            $parts[] = 'NULL';
        }

        if ($column->hasDefault) {
            $parts[] = 'DEFAULT ' . $this->compileDefaultValue($column->default);
        }

        // PostgreSQL enum emulation via CHECK constraint
        if ($column->type === ColumnType::Enum && ! empty($column->enumValues)) {
            $values = \array_map(fn (string $v): string => "'" . \str_replace(['\\', "'"], ['\\\\', "''"], $v) . "'", $column->enumValues);
            $parts[] = 'CHECK (' . $this->quote($column->name) . ' IN (' . \implode(', ', $values) . '))';
        }

        if ($column->checkExpression !== null) {
            $parts[] = 'CHECK (' . $column->checkExpression . ')';
        }

        // No inline COMMENT in PostgreSQL (use COMMENT ON COLUMN separately)

        return \implode(' ', $parts);
    }

    /**
     * PostgreSQL only supports STORED generated columns. Virtual generated columns
     * are rejected with UnsupportedException.
     *
     * @throws UnsupportedException if a VIRTUAL generated column is requested.
     */
    #[\Override]
    protected function compileGeneratedClause(Column $column): string
    {
        if ($column->generatedStored === false) {
            throw new UnsupportedException(
                'PostgreSQL does not support VIRTUAL generated columns. Use stored() instead.'
            );
        }

        return 'GENERATED ALWAYS AS (' . $column->generatedExpression . ') STORED';
    }

    /**
     * @param  string[]  $columns
     * @param  array<string, int>  $lengths
     * @param  array<string, string>  $orders
     * @param  array<string, string>  $collations
     * @param  list<string>  $rawColumns
     */
    public function createIndex(
        string $table,
        string $name,
        array $columns,
        bool $unique = false,
        string $type = '',
        string $method = '',
        string $operatorClass = '',
        array $lengths = [],
        array $orders = [],
        array $collations = [],
        array $rawColumns = [],
        bool $concurrently = false,
    ): Statement {
        if ($method !== '' && ! \preg_match('/^[A-Za-z0-9_]+$/', $method)) {
            throw new ValidationException('Invalid index method: ' . $method);
        }
        if ($operatorClass !== '' && ! \preg_match('/^[A-Za-z0-9_.]+$/', $operatorClass)) {
            throw new ValidationException('Invalid operator class: ' . $operatorClass);
        }

        $keyword = $unique ? 'CREATE UNIQUE INDEX' : 'CREATE INDEX';

        if ($concurrently) {
            $keyword .= ' CONCURRENTLY';
        }

        $sql = $keyword . ' ' . $this->quote($name)
            . ' ON ' . $this->quote($table);

        if ($method !== '') {
            $sql .= ' USING ' . \strtoupper($method);
        }

        $indexType = $unique ? IndexType::Unique : ($type !== '' ? IndexType::from($type) : IndexType::Index);
        $index = new Index($name, $columns, $indexType, $lengths, $orders, $method, $operatorClass, $collations, $rawColumns);

        $sql .= ' (' . $this->compileIndexColumns($index) . ')';

        return new Statement($sql, [], executor: $this->executor);
    }

    public function dropIndex(string $table, string $name): Statement
    {
        return new Statement(
            'DROP INDEX ' . $this->quote($name),
            [],
            executor: $this->executor,
        );
    }

    public function dropForeignKey(string $table, string $name): Statement
    {
        return new Statement(
            'ALTER TABLE ' . $this->quote($table)
            . ' DROP CONSTRAINT ' . $this->quote($name),
            [],
            executor: $this->executor,
        );
    }

    /**
     * Create a PL/pgSQL function.
     *
     * $body is emitted verbatim inside a dollar-quoted ($$...$$) block and must
     * come from trusted (developer-controlled) source — never from untrusted
     * input. A literal `$$` inside the body would close the dollar-quoted
     * string early and is rejected.
     *
     * @param  list<array{0: ParameterDirection, 1: string, 2: string}>  $params
     *
     * @throws ValidationException if $body contains a `$$` sequence.
     */
    public function createProcedure(string $name, array $params, string $body): Statement
    {
        $this->assertSafeDollarQuotedBody($body);

        $paramList = $this->compileProcedureParams($params);

        $sql = 'CREATE FUNCTION ' . $this->quote($name)
            . '(' . \implode(', ', $paramList) . ')'
            . ' RETURNS VOID LANGUAGE plpgsql AS $$ BEGIN ' . $body . ' END; $$';

        return new Statement($sql, [], executor: $this->executor);
    }

    public function dropProcedure(string $name): Statement
    {
        return new Statement('DROP FUNCTION ' . $this->quote($name) . '()', [], executor: $this->executor);
    }

    /**
     * Create a trigger backed by a PL/pgSQL function.
     *
     * $body is emitted verbatim inside a dollar-quoted ($$...$$) block and must
     * come from trusted (developer-controlled) source — never from untrusted
     * input. A literal `$$` inside the body would close the dollar-quoted
     * string early and is rejected.
     *
     * @throws ValidationException if $body contains a `$$` sequence.
     */
    public function createTrigger(
        string $name,
        string $table,
        TriggerTiming $timing,
        TriggerEvent $event,
        string $body,
    ): Statement {
        $this->assertSafeDollarQuotedBody($body);

        $funcName = $name . '_func';

        $sql = 'CREATE FUNCTION ' . $this->quote($funcName)
            . '() RETURNS TRIGGER LANGUAGE plpgsql AS $$ BEGIN ' . $body . ' RETURN NEW; END; $$; '
            . 'CREATE TRIGGER ' . $this->quote($name)
            . ' ' . $timing->value . ' ' . $event->value
            . ' ON ' . $this->quote($table)
            . ' FOR EACH ROW EXECUTE FUNCTION ' . $this->quote($funcName) . '()';

        return new Statement($sql, [], executor: $this->executor);
    }

    /**
     * Drop a trigger and its backing PL/pgSQL function. PostgreSQL requires
     * the table name (`DROP TRIGGER name ON table`); the function created by
     * {@see createTrigger()} is dropped at the same time so callers don't need
     * a separate {@see dropFunction()} call.
     *
     * @throws ValidationException if $table is null.
     */
    #[\Override]
    public function dropTrigger(string $name, ?string $table = null): Statement
    {
        if ($table === null) {
            throw new ValidationException('PostgreSQL dropTrigger() requires the table name.');
        }

        $funcName = $name . '_func';

        $sql = 'DROP TRIGGER ' . $this->quote($name) . ' ON ' . $this->quote($table)
            . '; DROP FUNCTION ' . $this->quote($funcName) . '()';

        return new Statement($sql, [], executor: $this->executor);
    }

    /**
     * Reject bodies that would break out of the surrounding `$$ ... $$`
     * dollar-quoted string.
     *
     * @throws ValidationException if $body contains `$$`.
     */
    private function assertSafeDollarQuotedBody(string $body): void
    {
        if (\str_contains($body, '$$')) {
            throw new ValidationException('Procedure/trigger body must not contain the dollar-quote terminator "$$"');
        }
    }

    #[\Override]
    public function compileAlter(Table $table): Statement
    {
        $alterations = [];

        foreach ($table->columns as $column) {
            $keyword = $column->isModify ? 'ALTER COLUMN' : 'ADD COLUMN';
            if ($column->isModify) {
                $def = $keyword . ' ' . $this->quote($column->name)
                    . ' TYPE ' . $this->compileColumnType($column);
            } else {
                $def = $keyword . ' ' . $this->compileColumnDefinition($column);
            }
            $alterations[] = $def;
        }

        foreach ($table->renameColumns as $rename) {
            $alterations[] = 'RENAME COLUMN ' . $this->quote($rename->from)
                . ' TO ' . $this->quote($rename->to);
        }

        foreach ($table->dropColumns as $col) {
            $alterations[] = 'DROP COLUMN ' . $this->quote($col);
        }

        foreach ($table->foreignKeys as $fk) {
            $def = 'ADD FOREIGN KEY (' . $this->quote($fk->column) . ')'
                . ' REFERENCES ' . $this->quote($fk->refTable)
                . ' (' . $this->quote($fk->refColumn) . ')';
            if ($fk->onDelete !== null) {
                $def .= ' ON DELETE ' . $fk->onDelete->toSql();
            }
            if ($fk->onUpdate !== null) {
                $def .= ' ON UPDATE ' . $fk->onUpdate->toSql();
            }
            $alterations[] = $def;
        }

        foreach ($table->dropForeignKeys as $name) {
            $alterations[] = 'DROP CONSTRAINT ' . $this->quote($name);
        }

        $statements = [];

        if (! empty($alterations)) {
            $statements[] = 'ALTER TABLE ' . $this->quote($table->name)
                . ' ' . \implode(', ', $alterations);
        }

        // PostgreSQL indexes are standalone statements, not ALTER TABLE clauses
        foreach ($table->indexes as $index) {
            $keyword = $index->type === IndexType::Unique ? 'CREATE UNIQUE INDEX' : 'CREATE INDEX';

            $indexSql = $keyword . ' ' . $this->quote($index->name)
                . ' ON ' . $this->quote($table->name);

            if ($index->method !== '') {
                $indexSql .= ' USING ' . \strtoupper($index->method);
            }

            $indexSql .= ' (' . $this->compileIndexColumns($index) . ')';
            $statements[] = $indexSql;
        }

        foreach ($table->dropIndexes as $name) {
            $statements[] = 'DROP INDEX ' . $this->quote($name);
        }

        return new Statement(\implode('; ', $statements), [], executor: $this->executor);
    }

