    public function testCreateProcedureUsesFunction(): void
    {
        $schema = new Schema();
        $result = $schema->createProcedure(
            'update_stats',
            params: [[ParameterDirection::In, 'user_id', 'INT'], [ParameterDirection::Out, 'total', 'INT']],
            body: 'SELECT COUNT(*) INTO total FROM orders WHERE orders.user_id = user_id;'
        );

        $this->assertSame('CREATE FUNCTION "update_stats"(IN "user_id" INT, OUT "total" INT) RETURNS VOID LANGUAGE plpgsql AS $$ BEGIN SELECT COUNT(*) INTO total FROM orders WHERE orders.user_id = user_id; END; $$', $result->query);
