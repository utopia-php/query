    // CREATE TABLE — PostgreSQL types

    public function testCreateTableBasic(): void
    {
        $schema = new Schema();
        $result = $schema->create('users', function (Blueprint $table) {
            $table->id();
            $table->string('name', 255);
            $table->string('email', 255)->unique();
        });
        $this->assertBindingCount($result);

        $this->assertStringContainsString('"id" BIGINT', $result->query);
        $this->assertStringContainsString('GENERATED BY DEFAULT AS IDENTITY', $result->query);
        $this->assertStringContainsString('"name" VARCHAR(255)', $result->query);
        $this->assertStringContainsString('PRIMARY KEY ("id")', $result->query);
        $this->assertStringContainsString('UNIQUE ("email")', $result->query);
    }

    public function testCreateTableColumnTypes(): void
    {
        $schema = new Schema();
        $result = $schema->create('test_types', function (Blueprint $table) {
            $table->integer('int_col');
            $table->bigInteger('big_col');
            $table->float('float_col');
            $table->boolean('bool_col');
            $table->text('text_col');
            $table->datetime('dt_col', 3);
            $table->timestamp('ts_col', 6);
            $table->json('json_col');
            $table->binary('bin_col');
            $table->enum('status', ['active', 'inactive']);
        });
        $this->assertBindingCount($result);

        $this->assertStringContainsString('INTEGER NOT NULL', $result->query);
        $this->assertStringContainsString('BIGINT NOT NULL', $result->query);
        $this->assertStringContainsString('DOUBLE PRECISION NOT NULL', $result->query);
        $this->assertStringContainsString('BOOLEAN NOT NULL', $result->query);
        $this->assertStringContainsString('TEXT NOT NULL', $result->query);
        $this->assertStringContainsString('TIMESTAMP(3) NOT NULL', $result->query);
        $this->assertStringContainsString('TIMESTAMP(6) WITHOUT TIME ZONE NOT NULL', $result->query);
        $this->assertStringContainsString('JSONB NOT NULL', $result->query);
        $this->assertStringContainsString('BYTEA NOT NULL', $result->query);
        $this->assertStringContainsString("CHECK (\"status\" IN ('active', 'inactive'))", $result->query);
    }

