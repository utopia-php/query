    // PROCEDURES — CREATE FUNCTION

    public function testCreateProcedureUsesFunction(): void
    {
        $schema = new Schema();
        $result = $schema->createProcedure(
            'update_stats',
            params: [['IN', 'user_id', 'INT'], ['OUT', 'total', 'INT']],
            body: 'SELECT COUNT(*) INTO total FROM orders WHERE orders.user_id = user_id;'
        );

        $this->assertStringContainsString('CREATE FUNCTION "update_stats"', $result->query);
        $this->assertStringContainsString('LANGUAGE plpgsql', $result->query);
        $this->assertStringNotContainsString('CREATE PROCEDURE', $result->query);
    }

    public function testDropProcedureUsesFunction(): void
    {
        $schema = new Schema();
        $result = $schema->dropProcedure('update_stats');

        $this->assertEquals('DROP FUNCTION "update_stats"', $result->query);
    }
    // TRIGGERS — EXECUTE FUNCTION

    public function testCreateTriggerUsesExecuteFunction(): void
    {
        $schema = new Schema();
        $result = $schema->createTrigger(
            'trg_updated_at',
            'users',
            timing: 'BEFORE',
            event: 'UPDATE',
            body: 'NEW.updated_at = NOW();'
        );

        $this->assertStringContainsString('EXECUTE FUNCTION', $result->query);
        $this->assertStringContainsString('CREATE TRIGGER "trg_updated_at"', $result->query);
        $this->assertStringNotContainsString('BEGIN SET', $result->query);
    }
